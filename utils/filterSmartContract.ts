export const filterContract = (content: string) => {
  const regex =
    /(\b(?=\w)function \w+\(?.+\))|(\b(?=\w)event \w+\(?.+\))|(\w+ (public|external) \w+)/gim;
  const parsed = content.split(regex);
  const functions = parsed.filter((a) => a?.match(regex));
  const events = parsed.filter((a) => {
    if (a?.match(/(\b(?=\w)function \w+\(?.+\))/)) {
      // return a.replace(/function|/);
    }
  });

  return { functions, events: [] } as IContractData;
};

